package com.sk.gfgProblems.arrays;

class Pair  {  
    long first, second;  
    public Pair(long first, long second)  
    {  
        this.first = first;  
        this.second = second;  
    }
	@Override
	public String toString() {
		return "Pair [first=" + first + ", second=" + second + "]";
	}
    
}

